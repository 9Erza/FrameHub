using FrameHub.Core.Models;
using FrameHub.Core.Services;

namespace FrameHub.App.Services;

public sealed class LocalizationService
{
    private readonly SettingsService _settingsService;
    private AppSettings _settings;

    public event EventHandler? LanguageChanged;

    public string CurrentLanguage => _settings.Language;

    public LocalizationService(SettingsService settingsService)
    {
        _settingsService = settingsService;
        _settings = _settingsService.Load();
    }

    public void SetLanguage(string languageCode)
    {
        string normalized = NormalizeLanguage(languageCode);
        if (_settings.Language == normalized)
        {
            return;
        }

        _settings.Language = normalized;
        _settingsService.Save(_settings);
        LanguageChanged?.Invoke(this, EventArgs.Empty);
    }

    public string T(string key)
    {
        var dictionary = CurrentLanguage == "pl" ? Polish : English;

        if (dictionary.TryGetValue(key, out string? value))
        {
            return value;
        }

        return English.TryGetValue(key, out string? fallback) ? fallback : key;
    }

    private static string NormalizeLanguage(string languageCode)
    {
        return languageCode.Equals("pl", StringComparison.OrdinalIgnoreCase) ? "pl" : "en";
    }

